'''
+ --> Adição
- --> Subtração
* --> Multiplicação
/ --> Divisão
// --> Divisão inteira (O resultado é arredondado)
** --> Exponenciação (Um numero elevado a outro)
% --> Módulo, resto da divisão
() --> Alterar a precedencia
'''

print('Adição', 10 + 10)
print('Subtração', 10 - 5)
print('Multiplicação', 10 * 5)
print('Divisão', 10 / 2)
print('Repetição', 7 * 'A')

print('Divisão inteira', 10.7 // 2.8)
print('Divisão inteira', 10 // 2)
print('Exponenciação', 2 ** 10)
print('Módulo', 10 % 4)
print('Precedencia', (5+2)*10)
